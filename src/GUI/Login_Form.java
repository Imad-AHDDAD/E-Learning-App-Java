    private void loginButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_loginButtonMouseClicked
        String username = usernameInput.getText();
        String password = passwordInput.getText();

        if (!username.isEmpty() && !password.isEmpty()) {
            String message = new User(username, password).login();
            if (message.equals("-1")) {
                passwordInput.setText("");
                JOptionPane.showMessageDialog(this, "incorrect password", "ELearning", JOptionPane.ERROR_MESSAGE);
            } else if (message.equals("-2")) {
                passwordInput.setText("");
                usernameInput.setText("");
                JOptionPane.showMessageDialog(this, "invalid username", "ELearning", JOptionPane.ERROR_MESSAGE);
            } else if (message.equals("admin")) {
                this.dispose();
                new Dashboard_admin().setVisible(true);
            } else if (message.equals("student")) {
                this.dispose();
                new DashboardStudent().setVisible(true);
            } else if (message.equals("teacher")) {
                this.dispose();
                new DashboardTeacher().setVisible(true);
            } else {
                JOptionPane.showMessageDialog(this, message, "ELearning", JOptionPane.DEFAULT_OPTION);
            }
        }else{
            JOptionPane.showMessageDialog(this, "Enter your login credentials", "ELearning", JOptionPane.DEFAULT_OPTION);
        }
    }//GEN-LAST:event_loginButtonMouseClicked

    private void showPasswordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_showPasswordActionPerformed
        if (showPassword.isSelected()) {
            passwordInput.setEchoChar((char) 0);
        } else {
            passwordInput.setEchoChar('*');
        }
    }//GEN-LAST:event_showPasswordActionPerformed

